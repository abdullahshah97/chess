package Game;

import Pieces.Piece;

/**
 *
 * @author abdullah
 */
public class Square {
    private Piece piece;
    public int x;
    public int y;
    
    public Square (int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Piece getPiece() {
        return piece;
    }

    public void setPiece(Piece piece) {
        this.piece = piece;
    }
    
    
}
